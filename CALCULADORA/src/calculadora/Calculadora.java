package calculadora;
//classe calculadora para desenvolver o funcionamento dos calculos matematicos que serão utilizados no classe "calculadora_teste".
public class Calculadora {
	
	//calculo de somar
	public int somar(int a, int b) {
		return a + b;
	}
	//calculo da subtração.
	public int subt(int a, int b) {
		return a - b;
	}
	//calculo da multriplicação.
	public float multi(float a, float b) {
		return a * b;
	}
	//calculo da divisão.
	public float divi(float a, float b) {
		return a / b;
	}
	
}
